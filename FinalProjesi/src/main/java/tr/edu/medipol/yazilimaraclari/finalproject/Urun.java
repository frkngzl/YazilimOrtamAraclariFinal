package tr.edu.medipol.yazilimaraclari.finalproject;

public class Urun {
	
	private String isim;
	private String id;
	
	
	public Urun(String isim, String id) {
		super();
		this.isim = isim;
		this.id = id;
	}


	public String getIsim() {
		return isim;
	}


	public String getId() {
		return id;
	}
	
	

}
